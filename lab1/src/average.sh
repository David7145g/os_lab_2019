#!/bin/bash

# Проверка, что есть хотя бы один аргумент
if [ $# -eq 0 ]; then
  echo "Ошибка! Необходимо указать хотя бы один аргумент."
  exit 1
fi

num_args=$#
sum=0

# Суммирование всех аргументов
for arg in "$@"; do
  sum=$((sum + arg))
done

# Вычисление среднего арифметического
average=$((sum / num_args))

echo "Среднее арифметическое: $average"